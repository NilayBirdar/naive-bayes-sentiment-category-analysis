package com.naive.bayes.app.chart;

import java.util.List;

public class CategoryChart {
	
	private List<String> categoryLabels;
	private List<Long> categoryValues;
	private List<String> categoryColors;
	public List<String> getCategoryLabels() {
		return categoryLabels;
	}
	public void setCategoryLabels(List<String> categoryLabels) {
		this.categoryLabels = categoryLabels;
	}
	public List<Long> getCategoryValues() {
		return categoryValues;
	}
	public void setCategoryValues(List<Long> categoryValues) {
		this.categoryValues = categoryValues;
	}
	public List<String> getCategoryColors() {
		return categoryColors;
	}
	public void setCategoryColors(List<String> categoryColors) {
		this.categoryColors = categoryColors;
	}
	
	

	
	

}
